package com.kartag.server.model;

import java.io.Serializable;

public class Handset implements Serializable{

	/**
	 * 
	 */
	private static final long serialVersionUID = 1437620518369576845L;

	private int id = 0;
	private String name = "";
	public void setId(int id) {
		this.id = id;
	}
	public int getId() {
		return id;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getName() {
		return name;
	}
}
