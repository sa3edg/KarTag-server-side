import java.util.ArrayList;

import com.kartag.common.presentation.forms.DefaultActionForm;
import com.kartag.server.model.Country;

public class CountryForm extends Country{

	
	/**
	 * 
	 */
	private static final long serialVersionUID = 4374713488071069473L;
	
	private ArrayList<? extends DefaultActionForm> countries;
	
	private String userId = "";
	
	public void setCountries(ArrayList<? extends DefaultActionForm> countries) {
		this.countries = countries;
	}

	public ArrayList<? extends DefaultActionForm> getCountries() {
		return countries;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}

	public String getUserId() {
		return userId;
	}
	
}

